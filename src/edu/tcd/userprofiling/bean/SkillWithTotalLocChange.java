package edu.tcd.userprofiling.bean;

public class SkillWithTotalLocChange {

	private String skill;

	private int totalLocChanged;

	private int userCount;

	public String getSkill() {
		return skill;
	}

	public void setSkill(String skill) {
		this.skill = skill;
	}

	public int getTotalLocChanged() {
		return totalLocChanged;
	}

	public void setTotalLocChanged(int totalLocChanged) {
		this.totalLocChanged = totalLocChanged;
	}

	public int getUserCount() {
		return userCount;
	}

	public void setUserCount(int userCount) {
		this.userCount = userCount;
	}

}
